// Per-drum exponential-envelope decay shift: each sample env -= env >> shift, so
// a larger shift = a longer ring. (time constant ~= 2^shift samples). Indexed by
// enum drum_type; the voice is freed once the envelope falls below ~1 LSB.
// Order matches enum drum_type.
